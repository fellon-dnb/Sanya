package com.sanya.client.core.api;
/**
 * Интерфейс для отправки аудиоданных.
 * Определяет контракт для компонентов, способных передавать аудиофрагменты
 * другим пользователям или сервисам.
 *
 * Назначение:
 *  - Абстрагировать логику отправки от конкретных реализаций (TCP, WebSocket и т.д.)
 *  - Упростить замену транспорта или реализацию разных стратегий доставки.
 *
 * Использование:
 *  Реализации могут быть предоставлены, например, через VoiceService или ChatConnector.
 */
public interface AudioSender {
    /**
     * Отправляет аудиофрагмент получателю.
     *
     * @param audioChunk массив байт аудиоданных
     * @param recipient  имя или идентификатор получателя
     */
    void send(byte[] audioChunk, String recipient);
}
