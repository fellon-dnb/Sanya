package com.sanya.client.core.api;

/**
 * Интерфейс репозитория ключей.
 * Определяет операции хранения и извлечения публичных ключей пользователей.
 *
 * Назначение:
 *  - Инкапсулировать доступ к хранилищу ключей (в памяти, БД, Redis и т.д.)
 *  - Обеспечить единый интерфейс для работы с ключами в криптографических модулях.
 *  - Упростить подмену реализации (например, для тестов или распределённого хранилища).
 *
 * Использование:
 *  Реализация может служить базой для KeyDirectory на клиенте
 *  или централизованного KeyStore на сервере.
 */
public interface KeyRepository {

    /**
     * Сохраняет публичный ключ пользователя.
     *
     * @param userId    идентификатор пользователя
     * @param publicKey публичный ключ в бинарном виде
     */
    void store(String userId, byte[] publicKey);

    /**
     * Возвращает публичный ключ пользователя.
     *
     * @param userId идентификатор пользователя
     * @return массив байт публичного ключа, либо null, если не найден
     */
    byte[] get(String userId);
}
