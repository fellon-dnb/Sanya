package com.sanya.client.core.api;

/**
 * Базовый интерфейс транспортного уровня.
 * Определяет общий контракт для компонентов, обеспечивающих сетевое взаимодействие.
 *
 * Назначение:
 *  - Абстрагировать сетевую реализацию (TCP, WebSocket, HTTP, UDP и т.д.)
 *  - Позволить легко заменять транспорт без изменения логики верхнего уровня.
 *  - Упростить тестирование сетевого слоя через mock-реализации.
 *
 * Использование:
 *  Может быть реализован, например, как ChatTransport (TCP) или WebSocketTransport.
 */
public interface Transport {

    /**
     * Устанавливает соединение с удалённым узлом.
     * Может бросать исключения при ошибке подключения.
     */
    void connect();

    /**
     * Отправляет произвольное сообщение через активное соединение.
     *
     * @param message объект для отправки
     */
    void send(Object message);

    /**
     * Закрывает транспорт и освобождает все ресурсы.
     * Должен корректно завершать фоновые потоки и соединения.
     */
    void close();
}
